package com.renren.ugc.comment.model;


/**
 * @author wangxx
 * 
 *  每条打印的数据
 */
public class OneLineData {
    
        private int count;
        
        private int max;
        
        private int avg;
        
        private int timeout;
        
        private int timeoutRate;
        
        private int exception;
        
        private int exceptionRate;
        
        private int miss;
        
        private int missRate;
        
        public int getCount() {
            return count;
        }

        
        public void setCount(int count) {
            this.count = count;
        }

        
        public int getMax() {
            return max;
        }

        
        public void setMax(int max) {
            this.max = max;
        }

        
        public int getAvg() {
            return avg;
        }

        
        public void setAvg(int avg) {
            this.avg = avg;
        }

        
        public int getTimeout() {
            return timeout;
        }

        
        public void setTimeout(int timeout) {
            this.timeout = timeout;
        }

        
        public int getTimeoutRate() {
            return timeoutRate;
        }

        
        public void setTimeoutRate(int timeoutRate) {
            this.timeoutRate = timeoutRate;
        }

        
        public int getException() {
            return exception;
        }

        
        public void setException(int exception) {
            this.exception = exception;
        }

        
        public int getExceptionRate() {
            return exceptionRate;
        }

        
        public void setExceptionRate(int exceptionRate) {
            this.exceptionRate = exceptionRate;
        }


        
        public int getMiss() {
            return miss;
        }


        
        public void setMiss(int miss) {
            this.miss = miss;
        }


        
        public int getMissRate() {
            return missRate;
        }


        
        public void setMissRate(int missRate) {
            this.missRate = missRate;
        }
        
        @Override
        public String toString(){
            return "count="+count+"|"+
                    "miss="+miss+"|"+
                    "miss-rate="+missRate+"|"+
                    "avg="+avg+"|"+
                    "timeout="+timeout+"|"+
                    "timeout-rate(%%)="+timeoutRate+"|"+
                    "exception="+exception+"|"+
                    "exception-rate(%%)="+exceptionRate+"|"+
                    "miss="+miss+"|"+
                    "miss-rate(%%)="+missRate;
                    
        }
        
        

}
