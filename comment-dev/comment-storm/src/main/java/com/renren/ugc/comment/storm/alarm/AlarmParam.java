package com.renren.ugc.comment.storm.alarm;



/**
 * @author xulei
 * @date 2013-8-8
 * @email lei.xu1@renren-inc.com
 * @tags 报警相关信息
 */
public class AlarmParam {
	
	/**	 报警产生的时间	**/
	private long time;
	/**	 报警的接口	**/
	private String method;
	/**	 报警内容	**/
	private String msg;
	/**	 报警标题	**/
	private String title;
	/**	  报警接受者电话，用逗号隔开	**/
	private String telNum;
	/**	  报警接受者邮箱，用逗号隔开	**/
	private String email;
	/**	  报警抄送邮箱，用逗号隔开	**/
	private String ccEmail;
	
	public long getTime() {
		return time;
	}
	public AlarmParam setTime(long time) {
		this.time = time;
		return this;
	}
	public String getMethod() {
		return method;
	}
	public AlarmParam setMethod(String method) {
		this.method = method;
		return this;
	}
	public String getMsg() {
		return msg;
	}
	public AlarmParam setMsg(String msg) {
		this.msg = msg;
		return this;
	}
	public String getTitle() {
		return title;
	}
	public AlarmParam setTitle(String title) {
		this.title = title;
		return this;
	}
	public String getTelNum() {
		return telNum;
	}
	public AlarmParam setTelNum(String telNum) {
		this.telNum = telNum;
		return this;
	}
	public String getEmail() {
		return email;
	}
	public AlarmParam setEmail(String email) {
		this.email = email;
		return this;
	}
	public String getCcEmail() {
		return ccEmail;
	}
	public AlarmParam setCcEmail(String ccEmail) {
		this.ccEmail = ccEmail;
		return this;
	}
	
}

